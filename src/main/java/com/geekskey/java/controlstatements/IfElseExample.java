package com.geekskey.java.controlstatements;

public class IfElseExample {

    public static void main(String[] args) {
        int a = 2;
        int b = 3;

        if (a == b) {
            System.out.println("We are in if block");
        } else {
            System.out.println("We are in else block");
        }
    }
}
