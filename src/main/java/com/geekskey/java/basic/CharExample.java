package com.geekskey.java.basic;

public class CharExample {

    public static void main(String[] args) {
        char x = 65;
        System.out.println("Value of x is : " + x);

        x = 'A';
        System.out.println("Value of x is : " + x);

        x = 36;
        System.out.println("Value of x is : " + x);
    }
}
